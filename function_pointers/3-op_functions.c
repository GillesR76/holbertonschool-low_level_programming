#include "3-calc.h"

/**
 * op_add - funtion that returns a sum
 * @a: int
 * @b: int
 *
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - funtion that returns a substraction
 * @a: int
 * @b: int
 *
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - funtion that returns a product
 * @a: int
 * @b: int
 *
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - funtion that returns a division
 * @a: int
 * @b: int
 *
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - funtion that returns a remainder
 * @a: int
 * @b: int
 *
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
